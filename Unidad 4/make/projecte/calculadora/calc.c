int suma(int op1, int op2){
    return (op1+op2);
}

int resta(int op1, int op2){
    return (op1-op2);
}

int multiplica(int op1, int op2){
    return (op1*op2);
}

int divideix(int op1, int op2){
    return (op1/op2);
}

int mayor(int op1, int op2){
    int n1=op1;
    int n2=op2;
    int n3=0;
    if (op1>op2)
    {
        n1=n3;
    }
    else
    {
        n2=n3;
    }
    return n3;
    
}